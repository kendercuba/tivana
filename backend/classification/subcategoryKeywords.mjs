export function findBestSubcategory(
  text,
  categoryId,
  subcategories
) {

  let best = null;
  let bestScore = 0;

  for (const sub of subcategories) {

    if (Number(sub.category_id) !== Number(categoryId)) continue;

    const keywords = subcategoryKeywords[sub.name.toLowerCase()];
    if (!keywords) continue;

    let score = 0;

    for (const kw of keywords) {
      if (text.includes(kw.toLowerCase())) {
        score++;
      }
    }

    if (score > bestScore) {
      bestScore = score;
      best = sub;
    }

  }

  return best;

}